class Month
{ 
  public static void main(String args[])
  {  	
	char  c='c';
		if(c=='a'|| c=='e'|| c=='i'|| c=='o'||c=='u')
		switch(c)
		{
		 case 'a':
			System.out.println("a for apple");
			break;
		case 'e':
			System.out.println("e for elephant");
			break;
		case 'i':
			System.out.println("i for ink");
			break;
		case 'o':
			System.out.println("o for ost");
			break;
		case 'u':
			System.out.println("u for ur");
			break;
			
		default:
			System.out.println("use vowels");


		}

	
  }
}
	